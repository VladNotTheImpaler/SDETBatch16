package class2;

public class StringConcatenationDemo {
    public static void main(String[] args) {

        String name= "John";
        int age=27;
        System.out.println(name+age); //will print without spaces
        System.out.println(name+" "+age); //will print with 1 space
        System.out.println(name+"   "+age); //will print with many spaces used in the ""

    }
}
