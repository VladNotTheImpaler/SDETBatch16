package class2;

public class FloatingPointNumbersDemo {
    public static void main(String[] args) {
        /*
        by default all the floating point numbers are doubles that`s why we need to write
        'f'/'F' at the end of the float type variables. but we don`t need to worry about it
         */
        float grocery=20.5F; // is a smaller size data type
        System.out.println(grocery);
        double price=11.22; // is the bigger can hold double the amount of float
        System.out.println(price);
        }
    }
