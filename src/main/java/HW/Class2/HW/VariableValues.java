package HW.Class2.HW;

public class VariableValues {
    public static void main(String[] args) {

        // Listing primitive variables
        byte byte1 = 1;
        short short2 = 22;
        int int3 = 333;
        long long4 = 4444L;
        float float5 = 55.55F;
        double double6 = 666.666;
        boolean boolean7 = false;
        char char8 = '8';

        // Updating our primitive variables
        byte1 = 2;
        short2 = 23;
        int3 = 334;
        long4 = 4445L;
        float5 = 55.56F;
        double6 = 666.667;
        boolean7 = true;
        char8 = '9';

        // Printing our variables
        System.out.println(byte1);
        System.out.println(short2);
        System.out.println(int3);
        System.out.println(long4);
        System.out.println(float5);
        System.out.println(double6);
        System.out.println(boolean7);
        System.out.println(char8);
    }
}
