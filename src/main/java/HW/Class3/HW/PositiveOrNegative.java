package HW.Class3.HW;

public class PositiveOrNegative {
    public static void main(String[] args) {
        int num = -5;

        if (num > 0) {
            System.out.println("Positive");
        }
        else if (num == 0) {
            System.out.println("Zero");
        }
        else if (num <= 0) {
            System.out.println("Negative");
        }

        }
    }
