package Class4;

public class E2IfElseStatement {
    public static void main(String[] args) {

        String name = "Jacob";
        //String is a non primitive data type and we can not use == sign
        if (name.equals("Vlad")) {
            System.out.println("I like Swimming");
        } else {
            System.out.println("I like programming");
        }
    }
}
