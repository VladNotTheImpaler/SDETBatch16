package Class7;

public class E2WhileLoops {
    public static void main(String[] args) {
        int num=0;
        // bellow we are using the increment operator

        num++; // 0 will turn into 1
        num++; // previous 1 will turn into 2
        System.out.println(num);

        int num2 = 10;
        num2--; // decremented by 1
        num2--; // 9 will turn into 8

    }
}
