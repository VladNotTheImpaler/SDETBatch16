package Class7;

public class E4WhileLoop {
    public static void main(String[] args) {
        //write a loop that prints numbers from 1 to 5
        //starting point is 1
        // how much is each step =>
        //ending point => 5
        int i = 1;
        while (i <= 5) {
            System.out.println(i);
            i++;
        }
        // can be done the same with FOR

        for (i = 1; i<= 5; i++){
            System.out.println(i);
        }

    }
}
