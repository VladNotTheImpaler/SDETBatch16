public class E11StringDemo {
    public static void main(String[] args) {

        String name="Today is Saturday";
        System.out.println(name.indexOf('i')); // to find the index for i
        System.out.println(name.indexOf('a')); // finds the index ONLY FOR 1st a
        // line 10 with star searching from index 4
        System.out.println(name.indexOf('a',4));


    }
}
